Kalasasaya <br>
Kalasasaya (kala = piedra; saya o sayasta = parado) o Templo de las Piedraa Paradas, tiene un diseño exacto vinculado con la astrología, desde este sitio se verificaban con la duración del año solar y el cambio de cada estación. En ambos equinoccios (otoño: 21 de marzo y primavera: 21 de septiembre) el Sol nacía por el centro de la puerta principal de ingreso. En el solsticio de invierno (21 de junio) el Sol nacía en el ángulo murario N.E. el solsticio de verano (21 de diciembre) se marcaba por el nacimiento del Sol en el ángulo murario S.E. Esta exactitud es un ejemplo del 
avance científico y técnico de la cultura Tiahuanaco. <br>

El Monolito de Ponce <br>
El Monolito de Ponce lleva el nombre del arqueólogo boliviano (Carlos Ponce) que lo descubrió en el año 1957.
 Es una de las figuras mejor logradas y conservadas de todo Tiahuanaco. La figura humana en sus manos sostiene un kero (vaso sagradao) y además se destaca las figuras de animales como el pumas, cóndores y águilas.</p>
